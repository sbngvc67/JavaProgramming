package day03_EscapeSequences;
/*
Escape Sequences: MUST be given double quote
    \n: starts a new line
    \t: tab
    \\: single back slash
    \": double quote
*/
public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Java \nPyton \nC#");
        System.out.println("--------------------");
        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice yo see you all! \nWhat class do we have next week?");

        System.out.println("------------------------------------");

        System.out.println("\tJava is cool Programming language");

        System.out.println("------------------------------------");

        System.out.println("/ \\");

        System.out.println("------------------------------------");
        System.out.println("\""); //in order to print one backward slash we need to give two backward slash

        System.out.println("------------------------------------");

        System.out.println("My favorite TV show is \"GAME OF THRONES\"");




    }


}
